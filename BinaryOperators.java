public class BinaryOperators
{
    public static void main(String args[])
    {
        System.out.println(5&6);    // Binary AND
        System.out.println(5|6);    // Binary OR
        System.out.println(5^6);    // Binary XOR
        System.out.println(~0);     // Binary One's Complement (Binary NOT)
        System.out.println(5<<2);   // Binray Left Shift
        System.out.println(6>>1);   // Binray Right Shift
    }
}