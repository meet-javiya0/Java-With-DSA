public class DataTypes
{
    public static void main(String args[])
    {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = false;
        System.out.println(var);
        float price = 10.5f;
        System.out.println(price);
        int num = 25;
        System.out.println(num);
        //long
        //double
        short n = 240;
        System.out.println(n);
    }
}